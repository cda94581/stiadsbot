const channelDelete = require('./channelDelete');
const channelPinsUpdate = require('./channelPinsUpdate');
const channelUpdate = require('./channelUpdate');
// const debug = require('./debug');
const emojiCreate = require('./emojiCreate');
const emojiDelete = require('./emojiDelete');
const emojiUpdate = require('./emojiUpdate');
// const error = require('./error');
const guildBanAdd = require('./guildBanAdd');
const guildBanRemove = require('./guildBanRemove');
const guildCreate = require('./guildCreate');
const guildDelete = require('./guildDelete');
const guildIntegrationsUpdate = require('./guildIntegrationsUpdate');
const guildMemberAdd = require('./guildMemberAdd');
const guildMemberAvailable = require('./guildMemberAvailable');
const guildMemberRemove = require('./guildMemberRemove');
const guildMembersChunk = require('./guildMembersChunk');
const guildMemberSpeaking = require('./guildMemberSpeaking');
const guildMemberUpdate = require('./guildMemberUpdate');
const guildUnavailable = require('./guildUnavailable');
const guildUpdate = require('./guildUpdate');
// const invalidated = require('./invalidated');
const inviteCreate = require('./inviteCreate');
const inviteDelete = require('./inviteDelete');
const message = require('./message');
const messageDelete = require('./messageDelete');
const messageDeleteBulk = require('./messageDeleteBulk');
const messageReactionAdd = require('./messageReactionAdd');
const messageReactionRemove = require('./messageReactionRemove');
const messageReactionRemoveAll = require('./messageReactionRemoveAll');
const messageReactionRemoveEmoji = require('./messageReactionRemoveEmoji');
const messageUpdate = require('./messageUpdate');
const presenceUpdate = require('./presenceUpdate');
const rateLimit = require('./rateLimit');
const ready = require('./ready');
const roleCreate = require('./roleCreate');
const roleDelete = require('./roleDelete');
const roleUpdate = require('./roleUpdate');
// const shardDisconnect = require('./shardDisconnect');
// const shardError = require('./shardError');
// const shardReady = require('./shardReady');
